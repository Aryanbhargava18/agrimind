def setup_knowledge_base() -> None:
    """Initialise ChromaDB with agronomy knowledge. Idempotent — safe to call multiple times."""
    global _collection

    log.info("Setting up knowledge base...")
    client = chromadb.EphemeralClient()
    emb_fn = embedding_functions.SentenceTransformerEmbeddingFunction(
        model_name="all-MiniLM-L6-v2"
    )
    _collection = client.get_or_create_collection(
        name="agronomy_kb",
        embedding_function=emb_fn
    )

    if _collection.count() == 0:
        _collection.add(
            documents=AGRONOMY_DOCS,
            ids=[f"doc_{i}" for i in range(len(AGRONOMY_DOCS))]
        )
        log.info(f"Knowledge base populated with {len(AGRONOMY_DOCS)} chunks.")
    else:
        log.info(f"Knowledge base already contains {_collection.count()} chunks.")

