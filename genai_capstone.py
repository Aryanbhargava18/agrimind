import joblib

def setup_model() -> None:
    """Load pre-trained model or train on crop_yield.csv if missing."""
    global _model, _scaler, _feature_cols

    MODEL_FILE = "model.joblib"
    
    if os.path.exists(MODEL_FILE):
        log.info(f"Loading persistent model from '{MODEL_FILE}'...")
        data = joblib.load(MODEL_FILE)
        _model = data['model']
        _scaler = data['scaler']
        _feature_cols = data['feature_cols']
        log.info("Model loaded successfully.")
        return

    log.info(f"Persistent model not found. Training on '{DATASET_PATH}' (this may take time)...")
    df = pd.read_csv(DATASET_PATH)

    # Drop nulls (mirrors M1)
    df = df.dropna()

    # Boolean → int
    df["Fertilizer_Used"] = df["Fertilizer_Used"].astype(int)
    df["Irrigation_Used"]  = df["Irrigation_Used"].astype(int)

    # IQR outlier clipping (mirrors M1)
    Q1    = df[NUM_COLS].quantile(0.25)
    Q3    = df[NUM_COLS].quantile(0.75)
    IQR   = Q3 - Q1
    df[NUM_COLS] = df[NUM_COLS].clip(Q1 - 1.5 * IQR, Q3 + 1.5 * IQR, axis=1)

    # One-hot encode categoricals (mirrors M1 — drop_first=True)
    X = pd.get_dummies(
        df.drop("Yield_tons_per_hectare", axis=1),
        columns=CAT_COLS,
        drop_first=True
    )
    y = df["Yield_tons_per_hectare"]

    _feature_cols = X.columns.tolist()

    # Standard scaling on numerical columns only (mirrors M1)
    _scaler  = StandardScaler()
    X[NUM_COLS] = _scaler.fit_transform(X[NUM_COLS])

    # Train
    _model = LinearRegression()
    _model.fit(X, y)
    
    # Save for next time
    joblib.dump({'model': _model, 'scaler': _scaler, 'feature_cols': _feature_cols}, MODEL_FILE)
    log.info(f"Model trained on {len(df):,} rows and saved to '{MODEL_FILE}'")


