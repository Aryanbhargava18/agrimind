# NODE 2 — RETRIEVE
# ─────────────────────────────────────────────────────────────────────────────
def node_retrieve(state: FarmState) -> dict:
    """Query ChromaDB for the most relevant agronomy context."""
    if state.get("error"):
        return {"retrieved_docs": []}

    log.info("[Node 2] Retrieving agronomy context...")
    farm  = state["farm_data"]
    query = (
        f"{farm['Crop']} {farm['Soil_Type']} {farm['Weather_Condition']} "
        f"{state['yield_category']} yield"
    )
    results = _collection.query(query_texts=[query], n_results=4)
    docs    = results["documents"][0]

    log.info(f"[Node 2] Retrieved {len(docs)} relevant chunks.")
    return {"retrieved_docs": docs}

# ─────────────────────────────────────────────────────────────────────────────
# NODE 3 — REASON
# ─────────────────────────────────────────────────────────────────────────────
def node_reason(state: FarmState) -> dict:
    """LLM agronomic reasoning over prediction + retrieved context."""
    if state.get("error"):
        return {"llm_reasoning": ""}

    log.info("[Node 3] Running LLM reasoning...")
    try:
        prompt = f"""You are a senior agronomist advising a farmer.

FARM DATA:
{json.dumps(state['farm_data'], indent=2)}

YIELD PREDICTION: {state['yield_prediction']:.2f} tons/hectare
YIELD CATEGORY: {state['yield_category']}

AGRONOMIC CONTEXT (from knowledge base):
{chr(10).join(f"- {d}" for d in state['retrieved_docs'])}

Based only on the information above:
1. Explain WHY this yield was predicted given the farm conditions.
2. Identify the TOP 3 risk factors affecting yield.
3. Suggest 3–5 SPECIFIC, actionable farming recommendations.

Be precise and practical. Do not hallucinate facts beyond what is provided."""

        reasoning = _call_llm(prompt)
        log.info("[Node 3] Reasoning complete.")
        return {"llm_reasoning": reasoning}

    except Exception as exc:
        log.error(f"[Node 3] LLM call failed: {exc}")
        return {"error": str(exc), "llm_reasoning": ""}

# ─────────────────────────────────────────────────────────────────────────────
# NODE 4 — GENERATE REPORT
# ─────────────────────────────────────────────────────────────────────────────
def node_report(state: FarmState) -> dict:
    """Convert LLM reasoning into a structured advisory report."""
    if state.get("error") and not state.get("llm_reasoning"):
        return {"advisory_report": _normalize_report({}, state)}

    log.info("[Node 4] Generating structured report...")
    try:
        prompt = f"""Convert the agronomic reasoning below into a structured JSON report.

REASONING:
{state['llm_reasoning']}

Return ONLY valid JSON with exactly these keys:
- recommended_actions: list of objects, each with "task" (short title) and "description" (full action)
- agronomic_references: list of strings citing the agronomic principles used
- safety_disclaimer: a one-sentence disclaimer about consulting professionals

No markdown, no explanation — pure JSON only."""

        raw_json = _call_llm(prompt, json_mode=True)
        report   = _normalize_report(json.loads(raw_json), state)

        log.info("[Node 4] Report generated successfully.")
        return {"advisory_report": report}

    except Exception as exc:
        log.error(f"[Node 4] Report generation failed: {exc}")
        return {"advisory_report": _normalize_report({}, state)}

