# NODE 1 — PREDICT
# ─────────────────────────────────────────────────────────────────────────────
def node_predict(state: FarmState) -> dict:
    """Preprocess farm input and run yield prediction."""
    log.info("[Node 1] Running yield prediction...")
    try:
        data = state["farm_data"].copy()
        data["Fertilizer_Used"] = int(data["Fertilizer_Used"])
        data["Irrigation_Used"]  = int(data["Irrigation_Used"])

        row = pd.DataFrame([data])
        row = pd.get_dummies(row, columns=CAT_COLS, drop_first=True)
        row = row.reindex(columns=_feature_cols, fill_value=0)
        row[NUM_COLS] = _scaler.transform(row[NUM_COLS])

        prediction = float(_model.predict(row)[0])
        category   = (
            "Low"    if prediction < YIELD_LOW_THRESHOLD  else
            "High"   if prediction > YIELD_HIGH_THRESHOLD else
            "Medium"
        )

        log.info(f"[Node 1] Prediction: {prediction:.2f} t/ha | Category: {category}")
        return {"yield_prediction": prediction, "yield_category": category, "error": ""}

    except Exception as exc:
        log.error(f"[Node 1] Prediction failed: {exc}")
        return {"error": str(exc), "yield_prediction": 0.0, "yield_category": "Unknown"}

