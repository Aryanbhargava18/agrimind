# HELPER: LLM CALL
# ─────────────────────────────────────────────────────────────────────────────
def _call_llm(prompt: str, json_mode: bool = False) -> str:
    """Single wrapper for all Groq calls. Raises on failure."""
    kwargs = dict(
        model=MODEL_NAME,
        messages=[{"role": "user", "content": prompt}],
        temperature=0.3,
    )
    if json_mode:
        kwargs["response_format"] = {"type": "json_object"}

    response = get_groq().chat.completions.create(**kwargs)
    return response.choices[0].message.content

