# HELPER: NORMALIZE REPORT
# ─────────────────────────────────────────────────────────────────────────────
def _normalize_report(raw: dict, state: FarmState) -> dict:
    """Guarantee the advisory report has exactly the expected schema."""
    farm = state["farm_data"]

    def _to_action_list(actions) -> List[dict]:
        if not isinstance(actions, list):
            return []
        result = []
        for a in actions:
            if isinstance(a, dict):
                result.append({
                    "task":        a.get("task", "Recommendation"),
                    "description": a.get("description", str(a))
                })
            else:
                result.append({"task": "Recommendation", "description": str(a)})
        return result

    return {
        "crop_summary": {
            "crop_name":        farm.get("Crop", "N/A"),
            "region":           farm.get("Region", "N/A"),
            "yield_prediction": f"{state['yield_prediction']:.2f} tons/hectare",
            "yield_category":   state["yield_category"],
        },
        "field_status": {
            "soil_type":   farm.get("Soil_Type", "N/A"),
            "rainfall_mm": farm.get("Rainfall_mm", "N/A"),
            "temperature": f"{farm.get('Temperature_Celsius', 'N/A')} °C",
            "irrigation":  farm.get("Irrigation_Used", "N/A"),
            "fertilizer":  farm.get("Fertilizer_Used", "N/A"),
            "weather":     farm.get("Weather_Condition", "N/A"),
        },
        "yield_risk_level":     state["yield_category"],
        "recommended_actions":  _to_action_list(raw.get("recommended_actions", [])),
        "agronomic_references": [str(r) for r in raw.get("agronomic_references", [])],
        "safety_disclaimer":    raw.get(
            "safety_disclaimer",
            "Always consult a certified local agronomist before implementing recommendations."
        ),
    }

